public class ex2 {
    public static void main(String [] args){
        System.out.println("\\n \t\t\t\t\t Line break.");
        System.out.println("\\t \t\t\t\t\t Tabulation.");
        System.out.println("\\' \t\t\t\t\t Single Quote.");
        System.out.println("\\\"\t\t\t\t\t Double Quote.");
        System.out.println("\\\\  \t\t\t\t\t \\Sign.");
        System.out.println("\\\\\\\\\\  \t\t\t\t\t \\\\Sign.");
        System.out.println("// \t\t\t\t\t Line Comment.");
        System.out.println("/*..*/ \t\t\t\t\t \n \t\t\t\t\t Block Comment.");
        System.out.println("\"\"\" \t\t\t\t\t \n \t\t\t\t\t Text Block.");
        System.out.println("\"\"\"");
    }
}
