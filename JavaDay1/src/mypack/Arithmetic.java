package mypack;

public class Arithmetic {
    public int sum(int a, int b){
        return a+b;
    }
    public double divide(int a, int b){
        return a/b;
    }

    public int minus(int a, int b){
        return a-b;
    }

    public int multiply(int a, int b){
        return a*b;
    }

    public int modulo(int a, int b){
        return a%b;
    }
}
